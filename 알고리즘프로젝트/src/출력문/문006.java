package 출력문;

public class 문006 {

	public static void main(String[] args) {
		System.out.println(16230); //16230
		// 문자열 "" 필수 System.out.println(abc);
		System.out.println(13-2.0); //11.0
		System.out.println(12+4/2+1); //12+2+1=15
		System.out.println("A"+"B"+"C"); //ABC
		//문자는 연산하지못한다 System.out.println("A"-"B");
		System.out.println("A"+"B"); //AB
		//문자와 숫자같이 연산불가 System.out.println(52-"33");
	}

}
